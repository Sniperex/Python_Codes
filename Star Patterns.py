#Equilateral Traingle
n=30
for i in range(1,11):
    print(' '*n + '* '*i)
    n=n-1
 

#Right Triangle   
n=30
for i in range(1,11):
    print(' '*n + "* "*i)

    
#Left Triangle
n=30
